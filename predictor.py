def run_prediction_pipeline(
    input_filepath: str,
    output_filepath: str,
    output_format: str = "excel",
) -> pd.DataFrame:
    """
    Full prediction flow: load model → load input → score → tier → explain → report.

    This is the entry point called by main.py's 'predict' subcommand.

    Args:
        input_filepath  (str): Path to the analyst input file (.xlsx, .csv, or .pdf).
        output_filepath (str): Path where the output report will be written.
        output_format   (str): "excel" (default) or "pdf".

    Returns:
        pd.DataFrame: Final scored, tiered, and explained DataFrame.
    """
    from data_loader import load_input_file, validate_dataframe
    from model_trainer import load_artifacts
    from report_generator import generate_excel_report, generate_pdf_summary

    # --- 1. Load model + metadata ---
    pipeline, metadata = load_artifacts()
    feature_columns    = metadata.get("feature_columns", FEATURE_CONFIG["FEATURE_COLUMNS"])
    training_stats     = metadata.get("training_stats", None)

    if not feature_columns:
        raise ValueError(
            "The loaded model has no feature columns in its metadata. "
            "Retrain the model after defining FEATURE_COLUMNS in config.py."
        )

    # --- 2. Load analyst input ---
    df_input = load_input_file(input_filepath)
    validate_dataframe(df_input, feature_columns, context="analyst input file")

    # --- 3. Score ---
    df_scored = score_individuals(pipeline, df_input, feature_columns)

    # --- 4. Assign priority tiers ---
    df_tiered = assign_priority_tiers(df_scored)

    # --- 5. Generate explanations ---
    df_explained = generate_explanations(
        pipeline, df_tiered, feature_columns, training_stats
    )

    # --- 6. Write output report ---
    if output_format == "pdf":
        generate_pdf_summary(df_explained, output_filepath)
    else:
        generate_excel_report(df_explained, output_filepath)

    return df_explained
